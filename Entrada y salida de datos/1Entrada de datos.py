#entrada de datos

# Con esta instruccion se le asignan datos a la variable "nombre", 
# mediante el mensaje que se solicita. Esta sentencia acepta cadenas.
nombre = input("Digite su nombre: ")

print(f"Hola {nombre}")

#Ahora guardemos valores numericos enteros

numero = int(input("Por favor, digite un numero "))

print(f"El numero es {numero}")

#Ahora guardemos valores numericos de tipo flotante

numeroFlotante = float(input("Digite el valor flotante "))

print(f"El numero flotante es ahora: {numeroFlotante + 1}")