#La salida de datos en python

nombre = "Ruben"
edad = 19

#Esta es la forma mas basica por la que se pueden imprimir valores en la consola
print("\nHola ", nombre, ", tu edad es: ", edad)

# Contamos con otra forma para la salida de datos en consola con python
print("\nHola {} tu edad es de {}".format(nombre, edad))

# Otra forma un poco mas sencilla de mostrar valores en la consola
print(f"\nHola {nombre}, tu edad es {edad}")