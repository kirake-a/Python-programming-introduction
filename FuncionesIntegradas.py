# Las funciones integradas en python

# Usando estas funciones "int", "float", es que transformamos a las cadenas
# en valores numericos, como se ve en los ejemplos
n = int("10")  # Usado siempre que queramos que un valor se vuelva un entero
m = float("10.8")

# Ahora "o", representa un valor numerico pasandose a cadena
o = str(10)

# Un valor entero a un valor binario
p = bin(10)

# Un valor entero a hexadecimal
q = hex(10)

# El valor absoluto de un numero
r = abs(-8)     #Siempre regresa el v. absoluto de un numero

# Redondeo de valores numericos
s = round(5.4)

# La funcion len
t = len("Ruben")
'''Esta funcion len nos permite poder contar la cantidad de caracteres con las que
cuenta la cadena dada.'''